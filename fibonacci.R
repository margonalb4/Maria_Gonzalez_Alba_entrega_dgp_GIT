fibonaccimargonalb4 <- function(longserie) {
  # Creamos primero el vector que va a guardar los números
  # que generemos correspondientes a esta sucesión
  resfib <- numeric(longserie)
  # Sabemos que los 2 primeros términos de la sucesión son 0 y 1 
  # lo tenemos que poner manualmente
  resfib[1] <- 0
  resfib[2] <- 1
  # Los siguientes términos se calculan sumando los dos números 
  # anteriores a este:
  for (i in 3:longserie) {
    resfib[i] <- resfib[i - 1] + resfib[i - 2]
  }
  return(resfib)
}

